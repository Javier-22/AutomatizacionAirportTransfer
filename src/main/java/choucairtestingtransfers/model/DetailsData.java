package choucairtestingtransfers.model;

public class DetailsData {
    private String name,email,confirm_email,country_code,phone,pick_up_number,return_number;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getConfirm_email() {
        return confirm_email;
    }

    public void setConfirm_email(String confirm_email) {
        this.confirm_email = confirm_email;
    }

    public String getCountry_code() {
        return country_code;
    }

    public void setCountry_code(String country_code) {
        this.country_code = country_code;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPick_up_number() {
        return pick_up_number;
    }

    public void setPick_up_number(String pick_up_number) {
        this.pick_up_number = pick_up_number;
    }

    public String getReturn_number() {
        return return_number;
    }

    public void setReturn_number(String return_number) {
        this.return_number = return_number;
    }
}
